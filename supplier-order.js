function renderCart() {
  const tbody = document.getElementById("cartBody");
  tbody.innerHTML = "";

  cart.forEach((item, index) => {
    const rowTotal = item.price * item.qty;

    tbody.innerHTML += `
      <tr>
        <td>${item.code}</td>
        <td>${item.name}</td>
        <td>‚Ç©${(item.consumerPrice || item.price).toLocaleString()}</td>
        <td>‚Ç©${item.price.toLocaleString()}</td>
        <td><input type="number" value="${item.qty}" min="1" onchange="updateQty(${index}, this.value)"></td>
        <td>‚Ç©${rowTotal.toLocaleString()}</td>
        <td><button onclick="removeItem(${index})">ÏÇ≠Ï†ú</button></td>
      </tr>
    `;
  });

  calculateTotalWithShipping();
}


