function updateQty(index, value) {
  const newQty = parseInt(value, 10);
  if (isNaN(newQty) || newQty < 1) {
    alert("ÏàòÎüâÏùÄ 1Í∞ú Ïù¥ÏÉÅÏù¥Ïñ¥Ïïº Ìï©ÎãàÎã§.");
    renderCart();
    return;
  }
  cart[index].qty = newQty;
  renderCart();
}

